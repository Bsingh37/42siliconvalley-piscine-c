int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int s;

	s = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		s = -1;
	}
	if (-9 <= nb && nb <= 9)
		ft_putchar((nb * s) + '0');
	else
	{
		ft_putnbr((nb / 10) * s);
		ft_putnbr((nb % 10) * s);
	}
}
