#include <stdio.h>

int		match(char *s1, char *s2)
{
	char *ptr;

	if (!(*s1) && !(*s2))
		return (1);
	if (*s2 == '*' && *(s2 + 1) == '*')
		return (match(s1, s2 + 1));
	if (*s2 == '*')
	{
		ptr = s1;
		while (ptr == s1 || *(ptr - 1))
		{
			if (*ptr == *(s2 + 1))
			{
				if (match(ptr, s2 + 1))
					return (1);
			}
			ptr++;
		}
	}
	if (*s1 == *s2)
		return (match(s1 + 1, s2 + 1));
	return (0);
}
