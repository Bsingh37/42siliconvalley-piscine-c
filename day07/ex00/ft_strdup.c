#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		j;
	char	*var;

	i = 0;
	j = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	var = (char *)malloc(i);
	while (j < i)
	{
		var[j] = src[j];
		j++;
	}
	return (var);
}
